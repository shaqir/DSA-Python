# Creating different types of arrays/lists in Python

# Simple integer array
numbers = [1, 2, 3, 4, 5]
print("Numbers array:", numbers)

# Array with mixed data types
mixed_array = [1, "Hello", 3.14, True]
print("Mixed array:", mixed_array)

# Creating an array using list() constructor
chars = list('Python')
print("Characters array:", chars)

# Creating an array with repeated elements
repeated = [0] * 5
print("Repeated elements array:", repeated)

# Accessing elements
print("\nFirst element:", numbers[0])
print("Last element:", numbers[-1])